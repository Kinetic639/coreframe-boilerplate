import { type ClassValue, clsx } from "clsx";
import { twMerge } from "tailwind-merge";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export function generateSlug(name: string): string {
  return name
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-|-$)+/g, "");
}

export function calculateFullPath(parentPath: string | null, slug: string): string {
  if (!parentPath) return slug.toUpperCase();
  return `${parentPath}/${slug.toUpperCase()}`;
}
